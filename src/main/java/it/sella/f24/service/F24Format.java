package it.sella.f24.service;

public class F24Format {
	
	private String f24format1;
	private String f24format2;
	public String getF24format1() {
		return f24format1;
	}
	public void setF24format1(String f24format1) {
		this.f24format1 = f24format1;
	}
	public String getF24format2() {
		return f24format2;
	}
	public void setF24format2(String f24format2) {
		this.f24format2 = f24format2;
	}
	@Override
	public String toString() {
		return "F24Format [f24format1=" + f24format1 + ", f24format2=" + f24format2 + "]";
	}
	
}
